package com.alibaba.cloud.ai.lynxe.tool.message;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Input for SendAssistantMessageTool: the assistant message text to send to the user and
 * add to conversation memory.
 */
public class SendAssistantMessageInput {

	@JsonProperty("message")
	private String message;

	public SendAssistantMessageInput() {
	}

	public SendAssistantMessageInput(String message) {
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
